// Entry point do AWS CDK - Define o app e as stacks
// Este arquivo é o ponto de entrada principal para síntese e deploy da infraestrutura

import * as cdk from 'aws-cdk-lib';
import { AdServerlessStack } from '../lib/ad-serverless-stack';

// App CDK: representa a aplicação CloudFormation completa
const app = new cdk.App();

// Stack principal - contém toda a infraestrutura do ad serverless
// O nome da stack é usado no CloudFormation para identificar todos os recursos
new AdServerlessStack(app, 'AdServerlessStack', {
  // Env: define a região e conta AWS alvo
  // Em produção, use variáveis de ambiente ou CI/CD para injetar esses valores
  env: {
    account: process.env.CDK_DEFAULT_ACCOUNT,
    region: process.env.CDK_DEFAULT_REGION || 'us-east-1',
  },
  // Description: descrição da stack no console AWS
  description: 'Ad Serverless - Infraestrutura serverless para plataforma de ad tracking (Generated by AWS CDK)',
});
